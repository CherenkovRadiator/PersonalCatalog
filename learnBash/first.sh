#Setting an interpreter for the file using `#!` followed by the location of the interpreter.
#!/bin/bash

#Commands can be executed directly with each line.
ls /etc/

#Declating a variable. Can only use captial letters for the name of the variable.
NAME="Harry"

#`echo` is used to output anything. 
#The value of the variable can be accessed through `$` sign followed by the name of the variable.
echo $NAME

